import type { Metadata } from "next";
import { setRequestLocale } from "next-intl/server";
import { type Locale } from "@/i18n/config";
import { generateAlternates } from "@/lib/metadata";
import SocialMediaPageClient from "./client";

export async function generateMetadata({
  params,
}: {
  params: Promise<{ locale: string }>;
}): Promise<Metadata> {
  const { locale } = await params;

  const titles = {
    nl: "Social Media Management | Robuust Marketing",
    en: "Social Media Management | Robuust Marketing",
  };

  const descriptions = {
    nl: "Professioneel social media beheer voor Instagram, LinkedIn, Facebook en TikTok. Content creatie, planning en community management.",
    en: "Professional social media management for Instagram, LinkedIn, Facebook and TikTok. Content creation, planning and community management.",
  };

  return {
    title: titles[locale as Locale] || titles.nl,
    description: descriptions[locale as Locale] || descriptions.nl,
    alternates: generateAlternates("/diensten/social-media", locale),
    openGraph: {
      title: titles[locale as Locale] || titles.nl,
      description: descriptions[locale as Locale] || descriptions.nl,
    },
  };
}

export default async function SocialMediaPage({
  params,
}: {
  params: Promise<{ locale: string }>;
}) {
  const { locale } = await params;
  setRequestLocale(locale as Locale);

  return <SocialMediaPageClient />;
}
